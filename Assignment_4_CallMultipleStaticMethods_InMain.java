package assignments_AB45;

public class Assignment_4_CallMultipleStaticMethods_InMain {
	
	static void Addition()
	{
		
		System.out.println("Addition of 2 & 3 is : 5");
	}
	
	static void Subtraction()
	{
		System.out.println("Subtraction of 8 & 5 is : 3");
	}
	
	static void Multiplication()
	{
		System.out.println("Multiplication of 4 & 3 is : 12");
	}
	
	static void Division()
	{
		System.out.println("Division of 30 & 5 is : 6");
	}
	
	static void PrintName()
	{
		System.out.println("Java is wonderful language");
	}
	

	public static void main(String[] args) 
	{
		Addition();
		Subtraction();
		Multiplication();
		Division();
		PrintName();
	
	}

}
